// имя файла → артикул: срезаем случайный суффикс _xxxxxxxx,
// «2310-» (транспорт) → «2310C», «HR_3400» → «HR3400», «9013» → «90135» (крепления)
const FIX = { '9013': '90135', '9014': '90145', '9015': '90155' };
function articleOf(base, slug){
  let a = base.replace(/_[a-z0-9]{8}$/, '');
  a = a.replace(/^HR_/, 'HR');
  if (slug === 'transport') a = a.replace(/-$/, 'C');
  if (slug === 'wall-brackets' && FIX[a]) a = FIX[a];
  return a;
}

const data = [];
let unmatched = 0;
for (const [folder, slug, title] of CATS){
  const dir = path.join(SRC, folder);
  if (!fs.existsSync(dir)) { console.error('нет папки:', folder); continue; }
  const files = fs.readdirSync(dir).filter(f => /\.(jpe?g|png|webp)$/i.test(f));
  if (!files.length) continue;
  fs.mkdirSync(path.join(OUT, slug), { recursive: true });
  const items = [];
  for (const f of files){
    const art = articleOf(f.replace(/\.[^.]+$/, ''), slug);
    const name = NAMES[art] || ('Schavon, арт. ' + art);
    if (!NAMES[art]) { unmatched++; console.log('  без названия:', slug, art); }
    const safe = art.replace(/[^A-Za-z0-9-]/g, '');
    await sharp(path.join(dir, f))
      .resize(600, 600, { fit: 'contain', background: '#ffffff' })
      .flatten({ background: '#ffffff' })
      .jpeg({ quality: 80, mozjpeg: true })
      .toFile(path.join(OUT, slug, safe + '.jpg'));
    items.push({ art, name, img: 'assets/catalog/' + slug + '/' + safe + '.jpg' });
  }
  items.sort((a, b) => a.art.localeCompare(b.art, 'ru', { numeric: true }));
  data.push({ slug, title, items });
  console.log(slug, items.length);
}
fs.writeFileSync(path.join(OUT, 'catalog-data.json'), JSON.stringify(data, null, 1));
console.log('total:', data.reduce((s, c) => s + c.items.length, 0), 'items,', unmatched, 'unmatched');
